package com.ict01.grammar2;
class Ex01{
	public static void main(String[] args){
		//논리형 : boolean , true 또는 false를 저장
		//자료형 변수이름; //선언
		//변수이름 = 데이터 //데이터저장
		boolean bo1;
		bo1 = true;

		//변수호출 : 변수가 가지고있는 데이터를 내보낸다.
		System.out.println(bo1);
		bo1 = false;
		System.out.println(bo1);
		
		boolean bo2 = true;
		System.out.println(bo2);
		
	}
}